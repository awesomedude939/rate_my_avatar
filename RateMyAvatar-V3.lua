function formatfile(name,description,looping)
	if #description <= 0 then 
		description = "No description."
	end

	local tablesa = {
		["config"] = {
			["name"] = name,
			["description"] = description,
			["looping"] = looping,
			["file"] = ".RMAS"
		},
		["anim"] = {

		}
	}
	for i,v in pairs(editscroll:GetChildren()) do 
		if not v:IsA("UIGridLayout") and v.Name ~= "def" then 
			tablesa.anim[tostring(v.placeholderlayout.Text)] = {}
			tablesa.anim[tostring(v.placeholderlayout.Text)].index_ = tonumber(v.placeholderlayout)
			tablesa.anim[tostring(v.placeholderlayout.Text)].id = v.realimg.Text
			tablesa.anim[tostring(v.placeholderlayout.Text)].text = v.placeholdertxt.Text
			tablesa.anim[tostring(v.placeholderlayout.Text)].sleep = tonumber(v.placeholdertime.Text) or 0
		end
	end
	return tablesa
end

