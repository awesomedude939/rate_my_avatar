function createFile(_filename)
	local cloned = filedefault:Clone()
	local clone2 = filename:Clone()
	local clone3 = fileicon:Clone()
	clone3.Parent = clone2
	clone2.Parent = cloned
	cloned.Parent = ScrollingFrame
	cloned.Visible = true
	cloned.filename.Text = _filename
	ScrollingFrame.CanvasSize = ScrollingFrame.CanvasSize + UDim2.new(0,0,1.15,0)
	local file,extension = readfile(_filename), tostring(string.split(tostring(_filename),".")[2])
	printconsole(extension)
	if extension == "rmas" then 
		cloned.filename.fileicon.Image = "http://www.roblox.com/asset/?id=9439887949"
	elseif extension == "json" then 
		cloned.filename.fileicon.Image = "http://www.roblox.com/asset/?id=9439864635"
	elseif extension == "txt" then 
		cloned.filename.fileicon.Image = "http://www.roblox.com/asset/?id=9439851090"
	elseif isfolder(_filename) then
		cloned.filename.fileicon.Image = "http://www.roblox.com/asset/?id=9439857554"
	else 
		cloned.filename.fileicon.Image = "http://www.roblox.com/asset/?id=9439844549"
	end
	cloned.MouseButton1Click:Connect(function()
		if not isfolder(_filename) then
			local s,r = pcall(game:GetService("HttpService").JSONDecode,file)
			local valid = false
			if s and r then 
				pcall(function()
					if r.config and r.config.file and r.config.file == ".RMAS" then 
						valid = true
					end
				end)
			end
			if string.lower(extension):match("rmas") then 
				valid = true
			end
			if valid then
				selectedfile = game:GetService("HttpService"):JSONDecode(readfile(_filename))
				descriptiono.Text = selectedfile["config"].description
				nameo.Text = selectedfile["config"].name
			end
		else
			for i,v in pairs(ScrollingFrame:GetChildren()) do 
				if not v:IsA("UiCorner") and not v:IsA("UIListLayout") then 
					v:Destroy()
				end
			end
			for i,v in pairs(listfiles(_filename)) do 
				createFile(v)
			end
		end
	end)
	buttonfy(cloned,cloned.filename)
end


