local animplaying = false

function playanimation()
	local order = {}
	for i,v in pairs(editscroll:GetChildren()) do 
		if not v:IsA("UIGridLayout") and v.Name ~= "def" then 
			table.insert(order,v.LayoutOrder,v)
		end
	end
	if checklooped.Visible == false then 
		for i,v in pairs(order) do 
			if animplaying == false then break end
			wait(tonumber(v.placeholdertime.Text))
			game:GetService("ReplicatedStorage").CustomiseBooth:FireServer("Update",{["DescriptionText"]=v.placeholdertxt.Text,["ImageId"]=v.realimg.Text})
		end
	else 
		while true do 
			for i,v in pairs(order) do 
				if animplaying == false then break end
				wait(tonumber(v.placeholdertime.Text))
				game:GetService("ReplicatedStorage").CustomiseBooth:FireServer("Update",{["DescriptionText"]=v.placeholdertxt.Text,["ImageId"]=v.realimg.Text})
			end
			if animplaying == false then 
			    break
			end
		end
	end
end

